import type { ComponentType } from "react";
import Svg, { Path } from "react-native-svg";

// Icon API matches Lucide's: { size?: number; color?: string }. Drop a
// custom icon anywhere a Lucide icon is accepted (toolbar, menus, etc).
export interface IconProps {
	size?: number;
	color?: string;
}

export type Icon = ComponentType<IconProps>;

// makeIcon turns a single 24x24 SVG path string into a Lucide-shaped
// icon component. Multi-path icons should be authored as plain function
// components (see BorderBottomIcon below).
function makeIcon(d: string): Icon {
	return function IconComponent({ size = 24, color }: IconProps) {
		return (
			<Svg width={size} height={size} viewBox="0 0 24 24">
				<Path d={d} fill={color} />
			</Svg>
		);
	};
}

